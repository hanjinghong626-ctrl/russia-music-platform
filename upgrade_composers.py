composers = data['composers']
updated_count = 0
opera_count = 0

for slug, composer in composers.items():
    # 初始化source列表
    if 'source' not in composer:
        composer['source'] = ''
    
    # 添加belcanto来源标注
    if composer['source']:
        if 'belcanto.ru' not in composer['source']:
            composer['source'] += '；据belcanto.ru'
    else:
        composer['source'] = '据belcanto.ru'
    
    # 更新师承关系
    if slug in teacher_student_data:
        teacher_info = teacher_student_data[slug]
        
        # 更新学生列表 (mentor_of)
        existing_mentor_of = set(composer.get('mentor_of', []))
        for teacher in teacher_info.get('teachers', []):
            existing_mentor_of.add(teacher)
        composer['mentor_of'] = list(existing_mentor_of)
        
        # 更新受影响列表 (influenced_by)
        existing_influenced = set(composer.get('influenced_by', []))
        for student in teacher_info.get('students', []):
            existing_influenced.add(student)
        composer['influenced_by'] = list(existing_influenced)
        
        # 更新biography
        addition = teacher_info.get('biography_addition', '')
        if addition and 'bio_zh' in composer:
            if len(composer['bio_zh']) < 300:
                composer['bio_zh'] = composer['bio_zh'].rstrip('。') + '。' + addition
    
    # 补充歌剧词典中的作品
    if slug in operas_by_composer:
        operas = operas_by_composer[slug]
        existing_works = {w.get('title_ru', '') for w in composer.get('works', [])}
        
        for opera in operas:
            opera_name_ru = opera.get('opera', '')
            # 检查是否已存在
            if opera_name_ru and opera_name_ru not in existing_works:
                # 提取俄语名称中的中文翻译（如果有）
                title_ru = opera_name_ru
                title_zh = ''
                if '(' in opera_name_ru:
                    parts = opera_name_ru.split('(')
                    title_ru = parts[0].strip()
                    title_zh = parts[-1].rstrip(')')
                
                new_work = {
                    'title_ru': title_ru,
                    'title_zh': title_zh,
                    'year': opera.get('year'),
                    'genre_zh': '歌剧',
                    'genre_ru': 'Опера',
                    'description_zh': '据belcanto.ru。',
                    'source': '据belcanto.ru'
                }
                
                composer.setdefault('works', []).append(new_work)
                existing_works.add(opera_name_ru)
                opera_count += 1
    
    updated_count += 1

print(f"Updated {updated_count} composers")
print(f"Added {opera_count} operas from belcanto dictionary")

